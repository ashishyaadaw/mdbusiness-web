    <script>
        $(document).ready(function(){
 
            // Smooth scroll for navigation links
            $('a.nav-link').on('click', function(event) {
                // Make sure this.hash has a value before overriding default behavior
                if (this.hash !== "") {
                    // Prevent default anchor click behavior
                    event.preventDefault();

                    // Store hash
                    var hash = this.hash;

                    // Using jQuery's animate() method to add smooth page scroll
                    // The 70 is for the fixed header height
                    $('html, body').animate({
                        scrollTop: $(hash).offset().top - 70 
                    }, 800, function(){
                
                        // Add hash (#) to URL when done scrolling (default click behavior)
                        // window.location.hash = hash; // Commented out to keep URL clean
                    });
                    
                    // If in mobile view, close the menu after clicking a link
                    if ($(window).width() <= 992) {
                        $('.nav-links').removeClass('active');
                    }
                } 
            });

            // --- Desktop Dropdown Logic ---
            function initDesktopNav() {
                // Top-level dropdown
                $('.nav-links li.has-dropdown').hover(
                    function() {
                        if ($(window).width() > 992) { // Only run on desktop
                            $(this).children('.dropdown-menu').stop(true, true).slideDown(200);
                        }
                    }, 
                    function() {
                        if ($(window).width() > 992) { // Only run on desktop
                            $(this).children('.dropdown-menu').stop(true, true).slideUp(200);
                        }
                    }
                );

                // Nested submenus
                $('.dropdown-menu li.has-submenu').hover(
                    function() {
                        if ($(window).width() > 992) { // Only run on desktop
                            $(this).children('.submenu').stop(true, true).slideDown(200);
                        }
                    },
                    function() {
                        if ($(window).width() > 992) { // Only run on desktop
                            $(this).children('.submenu').stop(true, true).slideUp(200);
                        }
                    }
                );
            }
            
            initDesktopNav(); // Run on load
            
            // --- NEW: Mobile Navigation Logic ---

            // Open mobile menu
            $('.menu-icon').on('click', function() {
                $('.nav-links').addClass('active');
            });

            // Close mobile menu
            $('.close-icon').on('click', function() {
                $('.nav-links').removeClass('active');
            });

            // Mobile dropdown toggle (+)
            $('.mobile-icon').on('click', function() {
                // Find the submenu associated with this icon
                var $submenu = $(this).siblings('.dropdown-menu, .submenu');
                
                // Toggle the submenu
                $submenu.stop(true, true).slideToggle(300);
                
                // Change icon from + to - (add/remove)
                var icon = $(this);
                if (icon.attr('name') === 'add-outline') {
                    icon.attr('name', 'remove-outline');
                } else {
                    icon.attr('name', 'add-outline');
                }
            });

            // Re-init desktop nav on resize (to re-apply/disable hover)
            // And close mobile menu if resizing to desktop
            $(window).on('resize', function() {
                if ($(window).width() > 992) {
                    $('.nav-links').removeClass('active');
                    // Reset any open mobile submenus
                    $('.dropdown-menu, .submenu').stop(true, true).hide();
                    $('.mobile-icon').attr('name', 'add-outline');
                }
                // Re-initialize (this is simple and just re-binds)
                initDesktopNav();
            });

        });
    </script>
